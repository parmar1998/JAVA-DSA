package com.cn.loops;

public class EnhancedForloop {
		public static void main(String[] args) {
			int arr[]= {1,2,3,4,5,6,7,8};
			for(int i:arr) {
				System.out.print(i+" ");
			}
			System.out.println();
			String course[]= {"A","B","C"};
			for(String cn:course) {
				System.out.print(cn+" ");
			}
		}
	
}
